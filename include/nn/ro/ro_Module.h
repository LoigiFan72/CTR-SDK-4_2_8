#ifndef NN_RO_RO_MODULE_H_
#define NN_RO_RO_MODULE_H_

#include <nn/Handle.h>
#include <nn/Result.h>
#include <nn/types.h>
#include <nn/util.h>
#include <nn/config.h>


namespace nn {
namespace ro {

    //--------------------------------------------------------------------------- 
    //
    //
    //
    //
    //
    //
    //
    //--------------------------------------------------------------------------- 
    class Module : private util::NonCopyable<Module>
    {
    private:
        uptr    m_Dummy;

    public:
        //----------------------------------------------------------------------
        //
        //
        //
        //
        //
        //
        //----------------------------------------------------------------------
        class EnumerateCallback
        {
        public:
            //----------------------------------------------------------------------
            //
            //
            //
            //
            //
            //
            //
            //
            //
            //
            //
            //----------------------------------------------------------------------
            virtual bool operator()(Module* p) = 0;
        };

    public:
        ~Module()
        {
            Unload();
        }
        void operator delete(void* p)
        {
            NN_UNUSED_VAR(p);
            // Does nothing
        }

        //--------------------------------------------------------------------------- 
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //--------------------------------------------------------------------------- 
        Result  Link();

        //--------------------------------------------------------------------------- 
        //
        //--------------------------------------------------------------------------- 
        Result  Unlink();

        //--------------------------------------------------------------------------- 
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //--------------------------------------------------------------------------- 
        Result  Unload();

        //--------------------------------------------------------------------------- 
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //--------------------------------------------------------------------------- 
        bool    IsAllSymbolResolved() const;

        //--------------------------------------------------------------------------- 
        //
        //
        //
        //
        //
        //
        //--------------------------------------------------------------------------- 
        const char* GetName() const;

        //--------------------------------------------------------------------------- 
        //
        //
        //
        //
        //
        //
        //
        //
        //--------------------------------------------------------------------------- 
        void    DoInitialize();

        //--------------------------------------------------------------------------- 
        //
        //
        //
        //
        //
        //
        //
        //
        //--------------------------------------------------------------------------- 
        void    DoFinalize();


        //--------------------------------------------------------------------------- 
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //--------------------------------------------------------------------------- 
        template<typename T>
        T       GetPointer(const char* name) const
        { return reinterpret_cast<T>(GetAddress(name)); }

        //--------------------------------------------------------------------------- 
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //--------------------------------------------------------------------------- 
        template<typename T>
        T       GetPointer(s32 index) const
        { return reinterpret_cast<T>(GetAddress(index)); }

        //--------------------------------------------------------------------------- 
        //
        //
        //
        //--------------------------------------------------------------------------- 
        void GetRegionInfo(RegionInfo* pri);

        //--------------------------------------------------------------------------- 
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //--------------------------------------------------------------------------- 
        static void Enumerate(EnumerateCallback* p);

        //---------------------------------------------------------------------------
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //
        //---------------------------------------------------------------------------
        static Module*  Find(const char* pName);

    private:
        Module();
        void*   operator new(size_t size) throw ();
        uptr    GetAddress(const char* name) const;
        uptr    GetAddress(s32 index) const;

        uptr    GetHead() { return reinterpret_cast<uptr>(this); }
    };

} // end of namespace ro
} // end of namespace nn



#endif  // ifndef NN_RO_RO_MODULE_H_
