class FlagsEnum1 : public FlagsEnum<bit8, EnumT>
{
public:
    //---------------------------------------------------------------------------
    //
    //---------------------------------------------------------------------------
    FlagsEnum1() : FlagsEnum<bit8, EnumT>() {}

    //---------------------------------------------------------------------------
    //
    //
    //
    //---------------------------------------------------------------------------
    explicit FlagsEnum1(EnumT e) : FlagsEnum<bit8, EnumT>(e) {}

    //---------------------------------------------------------------------------
    //
    //
    //
    //---------------------------------------------------------------------------
    explicit FlagsEnum1(bit8  e) : FlagsEnum<bit8, EnumT>(e) {}

    FlagsEnum1<EnumT>& operator =(bit32 v)   { this->Replace(v); return *this; }
    FlagsEnum1<EnumT>& operator =(int v)     { this->Replace(v); return *this; }
};

